<template lang="html">
    <div>
        <div class="form-group">
            <label :id="id" :num_files="num_documents()">{{label}}</label>
            <template v-if="help_text">
                <HelpText :help_text="help_text" />
            </template>

            <template v-if="help_text_url">
  <HelpTextUrl :help_text_url="help_text_url"/>
</template>

            <template v-if="canViewComments">
  <template v-if="!showingComment">
    <a v-if="field_data.comment_value" href @click.prevent="toggleComment">
      <i style="color:red" class="fa fa-comment-o">&nbsp;</i>
    </a>
    <a v-else href @click.prevent="toggleComment">
      <i class="fa fa-comment-o">&nbsp;</i>
    </a>
  </template>
  <a href v-else @click.prevent="toggleComment">
    <i class="fa fa-ban">&nbsp;</i>
  </a>
</template>
            <div v-if="canViewDeficiencies">
                <div v-if="canEditDeficiencies">
                    <div v-if="!showingDeficiencies">
                        <a v-if="field_data.deficiency_value" href=""  @click.prevent="toggleDeficiencies"><i style="color:red" class="fa fa-exclamation-triangle">&nbsp;</i></a>
                        <a v-else href="" @click.prevent="toggleDeficiencies"><i class="fa fa-exclamation-triangle">&nbsp;</i></a>
                    </div>
                    <a href="" v-else  @click.prevent="toggleDeficiencies"><i class="fa fa-ban">&nbsp;</i></a>
                    <Comment :question="label" :name="name+'-deficiency-field'" v-show="showingDeficiencies" :field_data="field_data" :isDeficiency="true"/>
                </div>
                <div v-else-if="field_data.deficiency_value" style="color:red">
                    <i class="fa fa-exclamation-triangle">&nbsp;</i>
                    <span>{{field_data.deficiency_value}}</span>
                </div>
            </div>
            <div v-if="files">
                <div v-for="v in documents">
                    <p>
                        File: <a :href="v.file" target="_blank">{{v.name}}</a> &nbsp;
                        <span v-if="!readonly && v.can_delete">
                            <a @click="delete_document(v)" class="fa fa-trash-o" title="Remove file" :filename="v.name" style="cursor: pointer; color:red;"></a>
                        </span>
                        <span v-else>
                            <i class="fa fa-info-circle" aria-hidden="true" title="Previously submitted documents cannot be deleted" style="cursor: pointer;"></i>
                        </span>
                    </p>
                </div>
                <span v-if="show_spinner"><i class='fa fa-2x fa-spinner fa-spin'></i></span>
            </div>
            <div v-if="!readonly" v-for="n in repeat">
                <div v-if="isRepeatable || (!isRepeatable && num_documents()==0)">
                    <input :name="name" type="file" class="form-control" :data-que="n" :accept="fileTypes" @change="handleChange" :required="isRequired"/>
                </div>
            </div>

        </div>
        <Comment :question="label" :name="name+'-comment-field'" v-show="showingComment" :field_data="field_data" :required="isRequired"/>
    </div>
</template>

<script>
import { api_endpoints, helpers } from "@/utils/hooks";
import Comment from "./comment.vue";
import HelpText from "./help_text.vue";
import { mapGetters } from "vuex";
export default {
  props: {
    compliance_id: null,
    name: String,
    label: String,
    id: String,
    isRequired: String,
    help_text: String,
    field_data: Object,
    fileTypes: {
      default: function() {
        var file_types =
          "image/*," +
          "video/*," +
          "audio/*," +
          "application/pdf,text/csv,application/msword,application/vnd.ms-excel,application/x-msaccess," +
          "application/x-7z-compressed,application/x-bzip,application/x-bzip2,application/zip," +
          ".dbf,.gdb,.gpx,.prj,.shp,.shx," +
          ".json,.kml,.gpx";
        return file_types;
      }
    },
    isRepeatable: Boolean,
    readonly: Boolean,
    docsUrl: String
  },
  components: { Comment, HelpText },
  data: function() {
    return {
      repeat: 1,
      files: [],
      showingComment: false,
      showingDeficiencies: false,
      show_spinner: false,
      documents: [],
      filename: null,
      help_text_url: ""
    };
  },
  computed: {
    ...mapGetters([
      "canViewComments",
      "canViewDeficiencies",
      "canEditDeficiencies"
    ]),
    csrf_token: function() {
      return helpers.getCookie("csrftoken");
    },
    compliance_document_action: function() {
      console.log(this.compliance_id);
      console.log(`/api/call_email/${this.compliance_id}/process_document/`);

      return this.compliance_id
        ? `/api/call_email/${this.compliance_id}/process_document/`
        : "";
    },
    value: function() {
      return this.field_data.value;
    }
  },

  methods: {
    toggleComment() {
      this.showingComment = !this.showingComment;
    },
    toggleDeficiencies: function() {
      if (this.showingDeficiencies) {
        this.field_data.deficiency_value = "";
      }
      this.showingDeficiencies = !this.showingDeficiencies;
    },
    handleChange: function(e) {
      this.show_spinner = true;
      if (this.isRepeatable) {
        let el = $(e.target).attr("data-que");
        let avail = $("input[name=" + e.target.name + "]");
        avail = [
          ...avail.map(id => {
            return $(avail[id]).attr("data-que");
          })
        ];
        avail.pop();
        if (this.repeat == 1) {
          this.repeat += 1;
        } else {
          if (avail.indexOf(el) < 0) {
            this.repeat += 1;
          }
        }
        $(e.target).css({ display: "none" });
      } else {
        this.files = [];
      }
      this.files.push(e.target.files[0]);

      if (e.target.files.length > 0) {
        //vm.upload_file(e)
        this.save_document(e);
      }

      this.show_spinner = false;
    },

    /*
        upload_file: function(e) {
            let vm = this;
            $("[id=save_and_continue_btn][value='Save Without Confirmation']").trigger( "click" );
        },
		*/

    get_documents: function() {
      var formData = new FormData();
      formData.append("action", "list");
      formData.append("input_name", this.name);
      formData.append("csrfmiddlewaretoken", this.csrf_token);
      this.$http.post(this.compliance_document_action, formData).then(res => {
        this.documents = res.body;
        this.show_spinner = false;
      });
    },

    delete_document: function(file) {
      this.show_spinner = true;

      var formData = new FormData();
      formData.append("action", "delete");
      formData.append("document_id", file.id);
      formData.append("csrfmiddlewaretoken", this.csrf_token);

      this.$http.post(this.compliance_document_action, formData).then(res => {
        this.documents = this.get_documents();
        //vm.documents = res.body;
        this.show_spinner = false;
      });
    },

    uploadFile(e) {
      let _file = null;

      if (e.target.files && e.target.files[0]) {
        var reader = new FileReader();
        reader.readAsDataURL(e.target.files[0]);
        reader.onload = function(e) {
          _file = e.target.result;
        };
        _file = e.target.files[0];
      }
      return _file;
    },

    save_document: function(e) {
      var formData = new FormData();
      formData.append("action", "save");
      formData.append("compliance_id", this.compliance_id);
      formData.append("input_name", this.name);
      formData.append("filename", e.target.files[0].name);
      formData.append("_file", this.uploadFile(e));
      formData.append("csrfmiddlewaretoken", this.csrf_token);

      this.$http.post(this.compliance_document_action, formData).then(
        res => {
          this.documents = res.body;
        },
        err => {}
      );
    },

    num_documents: function() {
      if (this.documents) {
        return this.documents.length;
      }
      return 0;
    }
  },
  mounted: function() {
    this.documents = this.get_documents();
    if (this.value) {
      //vm.files = (Array.isArray(vm.value))? vm.value : [vm.value];
      if (Array.isArray(this.value)) {
        this.value;
      } else {
        var file_names = this.value.replace(/ /g, "_").split(",");
        this.files = file_names.map(function(file_name) {
          return { name: file_name };
        });
      }
    }
  }
};
</script>

